# URI (Identificador de Recurso Uniforme)
# Definição: É uma maneira genérica de identificar um recurso. Pode ser um URL, mas também pode ser apenas um identificador sem fornecer um caminho para acessar diretamente o recurso.
# Exemplo:
# urn:isbn:978-3-16-148410-0 → É um URI que identifica um livro pelo seu ISBN, mas não diz onde encontrá-lo na internet.
# URL (Localizador de Recurso Uniforme)
# Definição: É um tipo de URI que não apenas identifica o recurso, mas também fornece um caminho para acessá-lo, geralmente incluindo o protocolo (como HTTP, HTTPS).
# Exemplos:
# https://www.example.com/ → URL que aponta para uma página da web.
# ftp://ftp.example.com/file.txt → URL que indica onde um arquivo pode ser baixado via protocolo FTP.


